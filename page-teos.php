                  <h2>
                    Omistuskirjoitus
                  </h2>
                  <div class="h3">
                    2018
                  </div>
                  <p>
                    <i>Omistuskirjoitus</i> on kuvataiteilija Otto Karvosen toteuttama julkinen taideteos Helsingin keskustakirjasto Oodiin. Teos sijoittuu kirjaston yleisöportaisiin, jotka kiertyvät läpi koko rakennuksen, katutasosta aina ylimpään
                    kerrokseen. Teos koostuu eri ihmisryhmille osoitetuista omistuskirjoituksista, jotka on maalattu portaikon seinämiin.
                  </p>
                  <p>
                    Teokseen kerättiin omistuskirjoituksia julkisella kampanjalla; kuka tahansa sai vapaasti ehdottaa, kenelle kirjasto tulisi omistaa. Ehdotuksissa pyydettiin kuitenkin keskittymään sellaisiin ihmisryhmiin, joiden voidaan katsoa olevan
                    yleisesti tunnistettuja. Myös loukkaavat, mielipiteeseen perustuvat tai muuten kyseenalaiset ilmaisut pyrittiin karsimaan joukosta pois.
                  </p>
                  <p>
                    Vaikka teos satoine omistuskirjoituksineen luo runsauden vaikutelmaa, on siihen päätynyt valikoima lopulta hyvinkin pieni ja sattumanvarainen. Se on näin ollen auttamattoman puutteellinen katsaus ihmisten ja ihmisenkaltaisten loputtomaan
                    kirjoon. Teoksesta löytyy kuitenkin jokaiselle takuuvarmasti yksi tai useampi itselle sopiva määritelmä.
                  </p>
                </div>
                <div class="omistuskirjoitus-introduction" lang="sv">
                  <h2>
                    Dedikation
                  </h2>
                  <div class="h3">
                    2018
                  </div>
                  <p>
                    <i>Dedikation</i> är ett offentligt konstverk av bildkonstnären Otto Karvonen, i Helsingfors centrumbibliotek Ode. Verket är placerat kring bibliotekets allmänna trappor som går runt i hela byggnaden, från gatuplanet ända upp till
                    översta våningen. Verket består av en dedikation som är ägnad olika människogrupper och har målats på trappornas väggar.
                  </p>
                  <p>
                    Dedikationer för verket samlades in genom en offentlig kampanj. Vem som helst kunde fritt föreslå vem biblioteket skulle dediceras till. Man bad ändå att förslagen skulle fokusera på sådan människogrupper som kan anses vara allmänt
                    kända. Man försökte även att sålla bort yttranden som var kränkande, baserade på åsikter eller på annat sätt tvivelaktiga.
                  </p>
                  <p>
                    Trots att verket med sina hundratals dedikationer ger ett intryck av överflöd är urvalet som togs med sist och slutligen mycket litet och slumpmässigt. Därmed rör det sig ohjälpligen om en bristfällig översikt över den oändliga brokiga
                    mångfalden av människor och människoliknande varelser. Var och en hittar ändå garanterat en eller flera definitioner som passar dem själva.
                  </p>
                </div>
                <div class="omistuskirjoitus-introduction" lang="en">
                  <h2>
                    Dedication
                  </h2>
                  <div class="h3">
                    2018
                  </div>
                  <p>
                    <i>Dedication</i> is a public artwork created by visual artist Otto Karvonen at Helsinki Central Library Oodi. The artwork is situated at the main staircase of the library, which spiral up through the entire building, from the street
                    level to the top floor. The artwork consists of dedications addressed to different groups of people, which have been painted on the walls of the staircase.
                  </p>
                  <p>
                    Dedications for the work were collected through a public campaign; anyone could freely suggest who the library should be dedicated to. However, it was requested that the suggestions would focus on such groups of people that are commonly
                    recognised. Suggestions that were offensive, opinionative or otherwise questionable were aimed to be eliminated.
                  </p>
                  <p>
                    Although the work together with its hundreds of dedications create the sense of abundance, the final selection is very small and random. Therefore, it is an incorrigibly incomplete view of the infinite diversity of humans and human-like
                    beings. Nevertheless, the work will certainly provide one or more definitions that anyone can identify themselves in.
                  </p>
                </div>
