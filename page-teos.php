                  <h2>
                    Посвящение
                  </h2>
                  <div class="h3">
                    2018
                  </div>
                  <p>
                    <i>Посвящение</i> — это публичное произведение художника Отто Карвонена для Центральной библиотеки Хельсинки "Ооди". Работа расположена на винтовой лестнице библиотеки, которая проходит через все здание с первого до последнего этажа.
                    Художественное произведение состоит из посвящений различным группам людей, написанных на стенах лестницы.
                  </p>
                  <p>
                    Посвящения собирались посредством публичной кампании; любой желающий мог предложить, кому должна быть посвящена библиотека. Вместе с тем при сборе предложений просили сосредоточиться на группах людей, которые, в целом, можно
                    идентифицировать. Оскорбительные, основанные на мнении или иным образом сомнительные выражения старались удалять из списка.
                  </p>
                  <p>
                    Несмотря на то, что художественное произведение из сотен посвящений создает впечатление перенасыщенности, для него было отобрано весьма ограниченное количество предложений случайным образом. Итак, это всего лишь частичный обзор
                    бесконечного спектра людей и человекоподобных существ. Тем не менее, для каждого гарантированно найдется одно или несколько приемлемых определений.
                  </p>
