                  <h2>
                    Dedicatoria
                  </h2>
                  <div class="h3">
                    2018
                  </div>
                  <p>
                    <i>Dedicatoria</i> es una obra de arte realizada por el artista visual Otto Karvonen que está disponible públicamente en la biblioteca central de Helsinki, Oodi. La obra está situada en las escaleras de la biblioteca que rodean el
                      edificio, desde la calle hasta el piso superior, y está formada por dedicatorias destinadas a distintos grupos de gente que han sido pintadas en los laterales de la escalera.
                  </p>
                  <p>
                    Para esta obra se recogieron dedicatorias en una campaña pública: cualquier persona podía proponer libremente a quién dedicar la biblioteca. Sin embargo, se pidió que las propuestas se centraran en grupos de gente que pudieran
                    considerarse comúnmente reconocidos. También se intentaron eliminar las expresiones ofensivas, subjetivas o de dudosa credibilidad.
                  </p>
                  <p>
                    Aunque la obra, con sus cientos de dedicatorias, causa muchas impresiones, finalmente se eligió una muestra muy pequeña de forma aleatoria. Por este motivo, se trata de una imagen irremediablemente incompleta de la interminable variedad
                    de seres humanos y similares. En cualquier caso, con toda seguridad, cada persona podrá encontrar una o más definiciones con las que sentirse identificada.
                  </p>
                </div>
                <div class="omistuskirjoitus-introduction" lang="fr">
                  <h2>
                    Dédicace
                  </h2>
                  <div class="h3">
                    2018
                  </div>
                  <p>
                    <i>Dédicace</i> est l’œuvre d’art public de l’artiste Otto Karvonen réalisée à la bibliothèque centrale d’Helsinki Oodi. Cette œuvre est située dans les escaliers publics de la bibliothèque qui font tout le tour du bâtiment, du
                    rez-de-chaussée jusqu’à l’étage supérieur. Cette œuvre est formée des dédicaces qui s’adressent à différents groupes de personnes, et qui ont été peintes sur les parois murales des escaliers.
                  </p>
                  <p>
                    Ces dédicaces regroupées ont été collectées par le biais d’une campagne publique dans laquelle tout le monde a eu la possibilité de suggérer librement à qui la bibliothèque devait être dédiée. Dans ces suggestions, on a toutefois demandé
                    de se concentrer sur des groupes de personnes pouvant être généralement identifiés. Aussi, les termes blessants, basés sur des opinions, ou discutables d’une autre manière ont été supprimés de la liste.
                  </p>
                  <p>
                    Bien que cette œuvre crée une impression d’abondance avec ses centaines de dédicaces, seule une petite sélection a été retenue au hasard. Elle constitue ainsi un aperçu inexorablement incomplet de la diversité infinie des individus et la
                    variété humaine. Cette œuvre offre cependant assurément à chacun une ou plusieurs définitions où il peut se reconnaître.
                  </p>
                </div>
                <div class="omistuskirjoitus-introduction" lang="de">
                  <h2>
                    Die Widmung
                  </h2>
                  <div class="h3">
                    2018
                  </div>
                  <p>
                    <i>Die Widmung</i> ist ein öffentliches Kunstwerk des bildenden Künstlers Otto Karvonen in der Zentralbibliothek Oodi in Helsinki. Das Werk befindet sich im öffentlichen Treppenhaus der Bibliothek, das sich vom Straßenniveau bis in die oberste
                    Etage durch das gesamte Gebäude windet. Das Werk setzt sich zusammen aus Widmungsschriften für verschiedene Personengruppen, die an die Treppenwände gemalt sind.
                  </p>
                  <p>
                    Für das Werk wurden in einer öffentlichen Kampagne Widmungsschriften gesammelt; jeder durfte frei vorschlagen, wem die Bibliothek gehören sollte. In den Vorschlägen wurde jedoch erbeten, sich auf Personengruppen zu konzentrieren, die als
                    allgemein zu erkennen angesehen werden können. Ebenso wurde gewünscht, beleidigende, auf Meinungen basierende oder auf andere Weise fragwürdige Äußerungen auszuschließen.
                  </p>
                  <p>
                    Obwohl das Werk mit seinen Hunderten von Widmungen einen Eindruck von Fülle erweckt, ist die Auswahl, die darin aufgenommen wurde, letztendlich sehr klein und zufällig. Es ist also ein hoffnungslos unvollständiger Blick auf das
                    unendliche Spektrum von Menschen und dergleichen. In dem Werk findet jedoch garantiert jeder für sich eine oder mehrere treffende Definitionen.
                  </p>
                </div>
