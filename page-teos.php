                  <h2>
                    كلمات الإهداء
                  </h2>
                  <div class="h3">
                    2018
                  </div>
                  <p>
                    كلمات الإهداء عبارة عن عمل فنّي عام أنجزه الفنان التشكيلي أوتّو كارفونين (Otto Karvonen) للمكتبة العامة المركزية لهلسنكي أودي (Oodi). موقع العمل الفني هو أدراج السلالم الخاصة بالجمهور للمكتبة العامة، حيث أنها تدور حول كل المبنى من مستوى
                    الشارع حتى الطابق الأعلى. العمل الفني مكون من كلمات إهداء موجهة لمختلف الفئات من الناس، حيث أنه تم رسمها على جدران أدراج السلالم.
                  </p>
                  <p>
                    تم جمع كلمات الإهداء للعمل الفني من خلال حملة عامة؛ كان بإمكان أي شخص اقتراح لمن يتوجب تقديم كلمات إهداء المكتبة العامة. طلبنا على كل حال التركيز على تلك الفئات من الناس، التي من الممكن اعتبارها معروفة بشكل عام. كما أن التعابير المُهينة
                    أو المبنية على وجهة النظر أو تلك التي محل خلاف، طلبنا إبعادها من المجموعة.
                  </p>
                  <p>
                    على الرغم من أن العمل الفني مع مئات كلمات الإهداء الخاصة به يُعطى انطباع بالوفرة، فإن الخيارات التي انتهى بها المطاف إلى هناك هي في النهاية قليلة جداً وعشوائية. فيُعتبر بذلك نظرة عامة غير كافية عن الطيف اللانهائي للناس وأمثال البشر. من
                    المؤكد على كل حال أنه يوجد من ضمن العمل الفني تعبير واحد أو أكثر من تعبير لوصف كل شخص.
                  </p>
