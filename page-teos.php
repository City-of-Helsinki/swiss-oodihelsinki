                  <h2>
                    献辞
                  </h2>
                  <div class="h3">
                    2018
                  </div>
                  <p>
                    献辞は、 ビジュアルアーチストのオット・カルボネンがヘルシンキ中央図書館「オーディ」のために作成した作品です。作品は、図書館の地上階から最上階まで通じる一般者用螺旋階段で展示されています。作品は、階段の壁に描かれた様々なグループに捧げた献辞から成っています。 
                  </p>
                  <p>
                    作品の献辞は、誰でも自由に提案でき、図書館を誰に捧げるかをテーマにして一般公募されましたが、応募条件として誰にでもわかるようなグループのみに絞られました。また、気分を害したり、意見があったり、疑わしいものは、選考から外しました。
                  </p>
                  <p>
                    何百と言う献辞から成る作品は、様々な感銘を生みながらも、選集にまで至ったものは大変少なく、ランダムに決められました。これは、人と無限にある人それぞれの個性を表現するには救いがたいほど不十分な要約です。だれにでもその人に合う字句が、1つまたは数個作品から必ず見つかることでしょう。 
                  <p>
                </div>
                <div class="omistuskirjoitus-introduction" lang="zh">
                  <h2>
                    题词
                  </h2>
                  <div class="h3">
                    2018
                  </div>
                  <p>
                    题词是视觉艺术家Otto Karvonen为赫尔辛基市中心图书馆Oodi创作的公共艺术作品。作品位于图书馆的公共楼梯内，从底楼到顶层贯穿整个建筑。这部作品包括在楼梯墙上题词给不同的人群。
                  </p>
                  <p>
                    题词的收集是通过公共征集；任何人都可以自由提议谁应该拥有该图书馆。但是，这些提案要求将重点放在那些可以被普遍认可的人群上。带有侮辱性的观点或其他可疑的表达方式将从人群中排除。
                  </p>
                  <p>
                    尽管这部作品以其数百题词著述，给人留下了充实的印象，但最终的选择却是很小而随机的。因此，这是人类的无穷无尽的视角的无奈而残缺的观察。但是，从作品中肯定有对每个人一个或多个合适的定义。
                  </p>
